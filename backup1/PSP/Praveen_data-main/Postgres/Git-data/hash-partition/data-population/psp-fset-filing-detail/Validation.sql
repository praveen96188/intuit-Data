FROM PSPADM.PSP_FSET_FILING_DETAIL ffd,
     PSPADM.PSP_MONEY_MOVEMENT_TRANSACTION mmt
WHERE ffd.MONEY_MOVEMENT_TRANSACTION_FK = mmt.MONEY_MOVEMENT_TRANSACTION_SEQ
  AND ffd.COMPANY_FK is not null
  and ffd.COMPANY_FK = mmt.COMPANY_FK
  and ffd.FSET_FILING_DETAIL_SEQ in
      (SELECT FSET_FILING_DETAIL_SEQ FROM SKUMAR71.TEMP_PSP_FSET_FILING_DETAIL);