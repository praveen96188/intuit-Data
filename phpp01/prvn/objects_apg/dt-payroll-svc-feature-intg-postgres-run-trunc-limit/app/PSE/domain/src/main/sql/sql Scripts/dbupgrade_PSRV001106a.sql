-- PURPOSE: 
--   This script will create index for purging following 2 tables
--   However since the table is in two schemas it is broken into
--   two parts.
--
--     PSP_SOURCE_SYSTEM_TRANSMISSION => 'CRIS', created more than 10 days ago (part 1a)
--     PSP_BATCH_JOB_AUDIT_LOG,       => created more than 5 days ago          (part 1a)
--
-- LOGON AS : PSPADM


SET SERVEROUTPUT ON
SET HEADING      ON
SET LINESIZE     1000
SET PAGESIZE     0
SET DEFINE       OFF


SPOOL dbupgrade_PSRV001106a.log

SELECT USER AS LOGIN_ID FROM DUAL;
SELECT TO_CHAR(SYSDATE, 'MM.DD.YYYY HH24:MI') AS START_TIME FROM DUAL;


PROMPT .
PROMPT Gather the customer info prior to the purge ...

SELECT 'PSP_SOURCE_SYSTEM_TRANSMISSION = ' || COUNT(*) FROM PSP_SOURCE_SYSTEM_TRANSMISSION;
SELECT 'PSP_BATCH_JOB_AUDIT_LOG = '        || COUNT(*) FROM PSP_BATCH_JOB_AUDIT_LOG;


PROMPT .
PROMPT Add the new object so that the purge will run fast ...

CREATE INDEX PSP_SOURCE_SYSTEM_TRANSMIS_I1
  ON PSP_SOURCE_SYSTEM_TRANSMISSION (FROM_SOURCE_SYSTEM, CREATED_DATE)
  NOLOGGING COMPUTE STATISTICS
  TABLESPACE PSP_IDX01;
  
CREATE INDEX PSP_BATCH_JOB_AUDIT_LOG_I1
  ON PSP_BATCH_JOB_AUDIT_LOG (CREATED_DATE)
  NOLOGGING COMPUTE STATISTICS
  TABLESPACE PSP_IDX01;

ALTER TABLE PSP_SOURCE_SYSTEM_TRANSMISSION ENABLE ROW MOVEMENT;
ALTER TABLE PSP_BATCH_JOB_AUDIT_LOG        ENABLE ROW MOVEMENT;


PROMPT .
SELECT TO_CHAR(SYSDATE, 'MM.DD.YYYY HH24:MI') AS END_TIME FROM DUAL;
PROMPT Done.

SPOOL OFF