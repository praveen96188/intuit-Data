--PURPOSE       :CREATE VIEW ON PSP_BANK_ACCOUNT TO HIDE ACCOUNT_NUMBER AND TO PROVIDE AN 
-----------------ADDITIONAL COLUMN PAYCARD_FLAG TO DETERMINE WHETHER AN ACCOUNT IS PAYCARD
----------------- OR NOT. 

-- LOGON AS : PSPADM


SET SERVEROUTPUT ON
SET HEADING      ON
SET LINESIZE     1000
SET PAGESIZE     0
SET DEFINE       OFF


SPOOL dbupgrade_PSRV002076.log

SELECT 'User = ' || USER
  FROM DUAL;
  
SELECT 'Start Time = ' || TO_CHAR (SYSDATE, 'MM.DD.YYYY HH24:MI')
  FROM DUAL;


CREATE OR REPLACE FORCE VIEW "PSPADM"."V_PSP_BANK_ACCOUNT" ("BANK_ACCOUNT_SEQ", "VERSION", "CREATOR_ID", "CREATED_DATE", "MODIFIER_ID", "MODIFIED_DATE", "REALM_ID", "ACCOUNT_TYPE_CD", "BANK_NAME", "EFFECTIVE_DATE", "EXPIRATION_DATE", "ROUTING_NUMBER", "A_C_H_ENTRY_CLASS", "PAYCARD_FLAG")
AS
  SELECT BANK_ACCOUNT_SEQ,
    VERSION,
    CREATOR_ID,
    CREATED_DATE,
    MODIFIER_ID,
    MODIFIED_DATE,
    REALM_ID,
    ACCOUNT_TYPE_CD,
    BANK_NAME,
    EFFECTIVE_DATE,
    EXPIRATION_DATE,
    ROUTING_NUMBER,
    A_C_H_ENTRY_CLASS,
    decode(SUBSTR(ACCOUNT_NUMBER, 1, 3),'968',1,0)*
    decode(ROUTING_NUMBER,'031101169',1,0) PAYCARD_FLAG     
  FROM PSPADM.PSP_BANK_ACCOUNT; 

CREATE OR REPLACE SYNONYM EBIETL.V_PSP_BANK_ACCOUNT FOR PSPADM.V_PSP_BANK_ACCOUNT;


GRANT SELECT ON PSPADM.V_PSP_BANK_ACCOUNT TO EBIETL;



 
 SELECT 'END Time = ' || TO_CHAR (SYSDATE, 'MM.DD.YYYY HH24:MI')
   FROM DUAL;
   
spool off;