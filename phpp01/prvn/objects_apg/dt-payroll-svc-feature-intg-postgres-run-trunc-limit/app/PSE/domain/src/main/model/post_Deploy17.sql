update psp_company_event ue set ue.status_cd = 'Inactive' where UE.COMPANY_EVENT_SEQ in (
select distinct company_event_seq from psp_company_event ce 
join psp_company_event_detail cde on CE.COMPANY_EVENT_SEQ = CDE.COMPANY_EVENT_FK 
join psp_company c on CE.COMPANY_FK = C.COMPANY_SEQ 
join psp_event_type et on CE.EVENT_TYPE_CD = ET.EVENT_TYPE_CD
where ET.EVENT_GROUP_CD = 'Fraud'
and ET.EVENT_TYPE_CD != 'FraudFlagRemovedEvent'
and CDE.EVENT_DETAIL_TYPE_CD = 'FraudEventCategory'
and CDE.VALUE = 'Payroll' 
and C.IS_FLAGGED_FOR_FRAUD = '0'
and CE.STATUS_CD = 'Active' )
/
COMMIT;

update psp_company_event ue set ue.status_cd = 'Inactive' where UE.COMPANY_EVENT_SEQ in (
select distinct company_event_seq from psp_company_event ce 
join psp_company_event_detail cde on CE.COMPANY_EVENT_SEQ = CDE.COMPANY_EVENT_FK  
join psp_event_type et on CE.EVENT_TYPE_CD = ET.EVENT_TYPE_CD
where ET.EVENT_GROUP_CD = 'Fraud'
and ET.EVENT_TYPE_CD != 'FraudFlagRemovedEvent'
and CDE.EVENT_DETAIL_TYPE_CD = 'FraudEventCategory'
and CDE.VALUE = 'SignUp' 
and CE.STATUS_CD = 'Active'
and not exists 
    (select OHR.ON_HOLD_REASON_SEQ
     from psp_on_hold_reason ohr 
     where OHR.COMPANY_FK = CE.COMPANY_FK
     and OHR.ON_HOLD_REASON_CD = 'FraudReview'
     and OHR.EXPIRATION_DATE is null)
)
/
COMMIT;

