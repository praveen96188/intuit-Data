
--VALIDATE PSP ENTITLEMENT TABLES WITH TEMP_ENTITLE AND DISPLAY ANY DIFFERENCES

SELECT PE.ENTITLEMENT_STATE ENTITLEMENT_STATE_PSP,
       TE.ENTITLEMENT_STATE ENTITLEMENT_STATE_TEMP,
       PE.CONTACT_NAME CONTACT_NAME_PSP,
       TE.CONTACT_NAME CONTACT_NAME_TEMP,
       PE.SUBSCRIPTION_NUMBER SUBSCRIPTION_NUMBER_PSP,
       TE.SUBSCRIPTION_NUMBER SUBSCRIPTION_NUMBER_TEMP,
       PE.BILLING_ZIP_CODE BILLING_ZIP_CODE_PSP,
       TE.BILLING_ZIP_CODE BILLING_ZIP_CODE_TEMP,
       PE.CANCELLATION_REASON CANCELLATION_REASON_PSP,
       TE.CANCELLATION_REASON CANCELLATION_REASON_TEMP,
       PE.SUBSCRIPTION_END_DATE SUBSCRIPTION_END_DATE_PSP,
       TE.SUBSCRIPTION_END_DATE SUBSCRIPTION_END_DATE_TEMP,
       PE.ORDER_SOURCE_CD ORDER_SOURCE_CD_PSP,
       TE.ORDER_SOURCE ORDER_SOURCE_CD_TEMP,
       PE.ORDER_NUMBER ORDER_NUMBER_PSP,
       TE.ORDER_NUMBER ORDER_NUMBER_TEMP,
       PE.LICENSE_NUMBER LICENSE_NUMBER_PSP,
       TE.LICENSE_NUMBER LICENSE_NUMBER_TEMP,
       PE.CUSTOMER_ID CUSTOMER_ID_PSP,
       TE.CUSTOMER_ID CUSTOMER_ID_TEMP,
       PE.NEXT_CHARGE_DATE NEXT_CHARGE_DATE_PSP,
       TE.NEXT_CHARGE_DATE NEXT_CHARGE_DATE_TEMP,
       PE.PAYMENT_METHOD_TYPE PAYMENT_METHOD_TYPE_PSP,
       TE.PAYMENT_METHOD_TYPE PAYMENT_METHOD_TYPE_TEMP,
       PE.CREDIT_CARD_TYPE CREDIT_CARD_TYPE_PSP,
       TE.CREDIT_CARD_TYPE CREDIT_CARD_TYPE_TEMP,
       PE.CREDIT_CARD_NUMBER CREDIT_CARD_NUMBER_PSP,
       TE.CREDIT_CARD_NUMBER CREDIT_CARD_NUMBER_TEMP,
       PE.CREDIT_CARD_EXPIRATION CREDIT_CARD_EXPIRATION_PSP,
       TE.CREDIT_CARD_EXPIRATION CREDIT_CARD_EXPIRATION_TEMP,
       PE.ENTITLEMENT_OFFERING_CODE ENTITLEMENT_OFFERING_CODE_PSP,
       TE.ENTITLEMENT_OFFERING_CODE ENTITLEMENT_OFFERING_CODE_TEMP,
       PE.CONTACT_EMAIL CONTACT_EMAIL_PSP,
       TE.CONTACT_EMAIL CONTACT_EMAIL_TEMP,
       PEU.ENTITLEMENT_UNIT_STATUS ENTITLEMENT_UNIT_STATUS_PSP,
       TE.ENTITLEMENT_UNIT_STATUS ENTITLEMENT_UNIT_STATUS_TEMP,
       PEU.SERVICE_KEY SERVICE_KEY_PSP,
       TE.SERVICE_KEY SERVICE_KEY_TEMP,
       PEU.EXTENSION_KEY EXTENSION_KEY_PSP,
       TE.EXTENSION_KEY EXTENSION_KEY_TEMP,
       PEU.LAST_VALIDATION_DATE LAST_VALIDATION_DATE_PSP,
       TE.LAST_VALIDATION_DATE LAST_VALIDATION_DATE_TEMP,
       PEU.FED_TAX_ID FED_TAX_ID_PSP,
       TE.FED_TAX_ID FED_TAX_ID_TEMP
  FROM PSP_ENTITLEMENT PE, TEMP_ENTITLE TE, PSP_ENTITLEMENT_UNIT PEU
 WHERE     TE.LICENSE_NUMBER = PE.LICENSE_NUMBER
       AND PE.ENTITLEMENT_OFFERING_CODE = TE.ENTITLEMENT_OFFERING_CODE
       AND PEU.ENTITLEMENT_FK = PE.ENTITLEMENT_SEQ
       AND PEU.FED_TAX_ID = TE.FED_TAX_ID
       AND (   PE.ORDER_NUMBER != TE.ORDER_NUMBER
            OR PE.CUSTOMER_ID != TE.CUSTOMER_ID
            OR PE.NEXT_CHARGE_DATE != TE.NEXT_CHARGE_DATE
            OR PE.PAYMENT_METHOD_TYPE !=
                  DECODE (TE.PAYMENT_METHOD_TYPE,
                          'Credit Card', 'CC',
                          'Check', 'PAPERCHECK',
                          'EFT', 'EFT',
                          NULL)
            OR PE.CREDIT_CARD_TYPE != TE.CREDIT_CARD_TYPE
            OR PE.CREDIT_CARD_NUMBER != TE.CREDIT_CARD_NUMBER
            OR PE.CREDIT_CARD_EXPIRATION != TE.CREDIT_CARD_EXPIRATION
            --  OR PE.ENTITLEMENT_OFFERING_CODE != TE.ENTITLEMENT_OFFERING_CODE
            OR PE.CONTACT_EMAIL != TE.CONTACT_EMAIL
            OR PE.ENTITLEMENT_STATE !=
                  DECODE (te.ENTITLEMENT_STATE,
                          'Active', 'Enabled',
                          'Disabled')
            OR PE.CONTACT_NAME != TE.CONTACT_NAME
            OR PE.SUBSCRIPTION_NUMBER != TE.SUBSCRIPTION_NUMBER
            OR PE.BILLING_ZIP_CODE != TE.BILLING_ZIP_CODE
            OR PE.CANCELLATION_REASON != TE.CANCELLATION_REASON
            OR PE.SUBSCRIPTION_END_DATE != TE.SUBSCRIPTION_END_DATE
            OR PE.ORDER_SOURCE_CD !=
                  DECODE (ORDER_SOURCE,
                          'FallDM2011', 'Siebel',
                          'eStore', 'Estore',
                          NULL)
            OR PEU.ENTITLEMENT_UNIT_STATUS !=
                  DECODE (TE.ENTITLEMENT_UNIT_STATUS,
                          'Active', 'Activated',
                          'Deactivated')
            OR PEU.SERVICE_KEY != TE.SERVICE_KEY
            OR PEU.EXTENSION_KEY != TE.EXTENSION_KEY
            OR PEU.LAST_VALIDATION_DATE != TE.LAST_VALIDATION_DATE);