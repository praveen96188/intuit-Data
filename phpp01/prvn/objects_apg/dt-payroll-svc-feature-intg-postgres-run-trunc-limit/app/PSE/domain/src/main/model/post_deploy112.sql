UPDATE PSP_EFFECTIVE_DEPOSIT_FREQ
      SET     PAYMENT_TEMPLATE_FREQUENCY_FK=(SELECT PAYMENT_TEMPLATE_FREQUENCY_ID FROM PSP_PMT_TEMPLATE_FREQUENCY WHERE PAYMENT_TEMPLATE_FK='MA-M941-PAYMENT' AND PAYMENT_FREQUENCY_ID='QUARTERMONTHLY'),
              MODIFIED_DATE = SYS_EXTRACT_UTC(SYSTIMESTAMP), MODIFIER_ID='PSRV002717'      
    WHERE     COMPANY_AGENCY_PMT_TEMPLATE_FK IN (SELECT COMPANYAGENCY_PMTTEMPLATE_SEQ FROM PSP_COMPANYAGENCY_PMTTEMPLATE WHERE PAYMENT_TEMPLATE_FK='MA-M941-PAYMENT')
              AND PAYMENT_TEMPLATE_FREQUENCY_FK=(SELECT PAYMENT_TEMPLATE_FREQUENCY_ID FROM PSP_PMT_TEMPLATE_FREQUENCY WHERE PAYMENT_TEMPLATE_FK='MA-M941-PAYMENT' AND PAYMENT_FREQUENCY_ID='WEEKLY');

UPDATE PSP_COMPANY_LAW 
    SET     LAW_FK ='177',
        MODIFIED_DATE = SYS_EXTRACT_UTC(SYSTIMESTAMP), 
        MODIFIER_ID='PSRV002921' 
    WHERE LAW_FK='9'
        AND SOURCE_DESCRIPTION NOT LIKE '%DC%';


commit;