   QB_INFO_APPLICATION_ID         VARCHAR2 (100 CHAR),
   QB_INFO_LICENSE_NUMBER         VARCHAR2 (100 CHAR),
   AGREE_INFO_AGREE_CREATE_DATE   TIMESTAMP (6),
   AGREE_INFO_NAME                VARCHAR2 (100 CHAR),
   AGREE_INFO_SERVICE_TYPE        VARCHAR2 (100 CHAR),
   AGREE_INFO_SOURCE_ID           VARCHAR2 (100 CHAR),
   AGREE_INFO_SERVICE_KEY         VARCHAR2 (100 CHAR),
   AGREE_INFO_AGREE_SUB_TYPE      VARCHAR2 (100 CHAR),
   COMP_ADDRESS_LINE2             VARCHAR2 (800 CHAR),
   COMP_ADDRESS_LINE1             VARCHAR2 (800 CHAR),
   COMP_CITY                      VARCHAR2 (200 CHAR),
