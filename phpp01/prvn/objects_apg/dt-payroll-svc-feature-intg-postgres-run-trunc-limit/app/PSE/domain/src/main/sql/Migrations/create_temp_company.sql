CREATE SEQUENCE PSPADM.SEQ_TEMP_COMPANY INCREMENT BY 1 MINVALUE 1 NOMAXVALUE NOCYCLE NOCACHE NOORDER;

CREATE OR REPLACE TRIGGER PSPADM.TR_INS_TEMP_COMPANY
   BEFORE INSERT ON PSPADM.TEMP_COMPANY FOR EACH ROW
BEGIN
   SELECT PSPADM.SEQ_TEMP_COMPANY.NEXTVAL INTO :NEW.RECORD_PK FROM DUAL;
END;
