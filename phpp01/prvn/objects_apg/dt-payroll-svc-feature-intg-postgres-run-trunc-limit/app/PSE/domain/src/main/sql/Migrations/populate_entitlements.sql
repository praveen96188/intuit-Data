 PROMPT UPDATE PSP_ENTITLEMENT_UNIT WITH ITS ENTITLEMENT_FK

  MERGE INTO PSP_ENTITLEMENT_UNIT EU
       USING PSP_ENTITLEMENT E
          ON (EU.LICENSE_NUMBER = E.LICENSE_NUMBER
              AND EU.ENTITLEMENT_OFFERING_CODE = E.ENTITLEMENT_OFFERING_CODE)
  WHEN MATCHED
  THEN
     UPDATE SET EU.ENTITLEMENT_FK = E.ENTITLEMENT_SEQ;
    COMMIT; 
    
  PROMPT UPDATE PSP_ENTITLEMENT_UNIT WITH ITS COMPANY_FK
    
  
 MERGE INTO PSPADM.PSP_ENTITLEMENT_UNIT PEU
     USING (SELECT company_seq,SUBSCRIPTION_NUMBER, FED_TAX_ID
              FROM (SELECT SUBSCRIPTION_NUMBER,
                           FED_TAX_ID,
                           company_seq,
                           ROW_NUMBER ()
                           OVER (PARTITION BY PQI.SUBSCRIPTION_NUMBER, PC.FED_TAX_ID
                                 ORDER BY PC.MODIFIED_DATE DESC)
                              RN
                      FROM PSP_COMPANY PC,PSP_QUICKBOOKS_INFO PQI WHERE PQI.COMPANY_FK=PC.COMPANY_SEQ)
             WHERE rn = '1') SRC
        ON (PEU.FED_TAX_ID = SRC.FED_TAX_ID
            AND PEU.SUBSCRIPTION_NUMBER = SRC.SUBSCRIPTION_NUMBER)
WHEN MATCHED
THEN
   UPDATE SET PEU.COMPANY_FK = SRC.COMPANY_SEQ;
   COMMIT;
   
  
PROMPT UPDATE THE MIGRATION STATUS FOR ALL ENTITLEMENTS WITHOUT COMPANIES
UPDATE TEMP_ENTITLE TE
   SET TE.MIGRATION_STATUS = 'COMPANY NOT FOUND'
 WHERE EXISTS
          (SELECT 'T'
             FROM PSP_ENTITLEMENT_UNIT PEU
            WHERE     PEU.SUBSCRIPTION_NUMBER = TE.SUBSCRIPTION_NUMBER
                  AND PEU.FED_TAX_ID = TE.FED_TAX_ID
                  AND PEU.COMPANY_FK IS NULL);
    COMMIT;    

