/**
 * This is the PSP service API that deals with all company related information
 * <p>The API includes:
 * <p>Add/Update/Deactivate companies
 * <p>Add/Deactivate/Reactivate a service for a company
 * <p>Add/Update/Deactivate bank accounts for a company
 * <p>Add/Cancel Strikes
 */
public interface ICompanyManager {
    //******************************************************************************************
    //** Company
    //******************************************************************************************

    /**
     * Adds the passed company to PSP, associating it with the specified offering
     *
     * @param pCompany
     * @return Returns the ProcessResult that will contain the added Company domain entity and
     *         any messages generated by the processing.
     */
    ProcessResult<Company> addCompany(CompanyDTO pCompany);

    /**
     * Updates company in PSP
     *
     * @param pSourceSystemCode - the identifier of the source system
     * @param pSourceCompanyId  - the identifier of the company in the SPS
     * @param pCompany
     * @return
     */
    ProcessResult<Company> updateCompany(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyDTO pCompany);

    /**
     * Updates company in PSP and AccountService with REST call
     *
     * @param pSourceSystemCode - the identifier of the source system
     * @param pSourceCompanyId  - the identifier of the company in the SPS
     * @param pCompany
     * @return
     */
    ProcessResult<Company> updateCompanyWithAccountService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyDTO pCompany);

    /**
     * Updates a company rate in PSP
     *
     * @param pSourceSystemCd - The identifier of the source system
     * @param pSourceCompanyId  - The identifier of the company in the SPS
     * @param pLaw  - The law being updated
     * @param pQuarterStartDate - First day of the quarter being updated.
     * @param pRate - The new rate.
     * @param pPushToQuickbooks - Whether or not to push the change to Quickbooks.
     * @return
     */
    ProcessResult<CompanyLawRate> updateCompanyLawRate(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, Law pLaw,
                                                       SpcfCalendar pQuarterStartDate, Double pRate, boolean pPushToQuickbooks);

    /**
     * Updates PSID for an existing company
     *
     * @param pSourceSystemCode - the identifier of the source system
     * @param pSourceCompanyId  - the identifier of the company in the SPS
     * @param pNewSourceCompanyId
     * @return
     */
    ProcessResult<Company> updateSourceCompanyId(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pNewSourceCompanyId);    

    /**
     * Updates specific QB Company Info in PSP
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pCompany
     * @return
     */
    ProcessResult<Company> updateQBCompanyInfo(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyDTO pCompany);

    /**
     * Claims (or redeems) an offer for a company.
     *
     * @param pOfferCd The offer-code identifying the offer to be claimed
     * @param pCompany The company claiming the offer
     * @return A ProcessResult containing, when successful, the CompanyOffer entity relating the Company to the Offer
     */
    ProcessResult<CompanyOffer> claimOfferForCompany(String pOfferCd, String pPromotionId, Company pCompany);
    
    //******************************************************************************************
    //** Specialized company updates
    //******************************************************************************************

    /**
     * Updates the funding model for a company
     *
     * @param pSourceSystemCd
     * @param pSourceCompanyId
     * @param pNewFundingModel
     * @return
     */
    ProcessResult<Company> updateCompanyFundingModel(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, FundingModel pNewFundingModel);// Service

    /**
     * Updates a Company IsFlaggedForFraud flag
     *
     * @param pSourceSystemCode SourceSystemCode
     * @param pSourceCompanyId  String
     * @return ProcessResult<Company>
     */
    ProcessResult<Company> removeFraudFlag(SourceSystemCode pSourceSystemCode, String pSourceCompanyId);

    /**
     * Finds the next available source system ID for the given source system.  Currently only supported for QBDT
     *
     * @param pSourceSystemCd Source system to create source system ID for
     * @return Newly-generated source system ID
     */
    String createSourceCompanyId(SourceSystemCode pSourceSystemCd);

    /**
     * Finds the next available source system ID for the given source system.  Currently only supported for QBDT
     *
     * @param pSourceSystemCd Source system to create source system ID for
     * @param stateCode Two letter state code used to generate correct prefix for source system ID.
     * @return Newly-generated source system ID
     */
    String createSourceCompanyId(SourceSystemCode pSourceSystemCd, String stateCode);

    ProcessResult<CompanyNote> addCompanyNote(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, String pCompanyEventId, String pInsertUserId, String pNotes, boolean pAlert);

    //******************************************************************************************
    //** Company Service
    //******************************************************************************************

    /**
     * Add a payroll service to an existing company in PSP
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pServiceInfo
     * @return
     */
    ProcessResult<CompanyService> addService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, ServiceInfoDTO pServiceInfo);

    /**
     * Updates the information about a specific service in PSP
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pServiceInfo
     * @return
     */
    ProcessResult<CompanyService> updateService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, ServiceInfoDTO pServiceInfo);

    /**
     * Deactivates the given service for the given company
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pServiceCode
     * @return
     */
    ProcessResult<CompanyService> deactivateService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, ServiceCode pServiceCode);

    /**
     * Deactivates the given service for the given company
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pServiceCode
     * @return
     */
    ProcessResult<CompanyService> terminateService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, ServiceCode pServiceCode);

    /**
     * Reactivates a given service for a given company
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pServiceCode
     * @return
     */
    ProcessResult<CompanyService> reactivateService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, ServiceCode pServiceCode);

    /**
     * Updates the DD limits for a company
     *
     * @param pSourceSystemCd
     * @param pSourceCompanyId
     * @param pNewCompanyDDLimit
     * @param pNewEmployeeDDLimit
     * @return
     */
    ProcessResult<DDCompanyServiceInfo> updateDDLimits(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, SpcfMoney pNewCompanyDDLimit, SpcfMoney pNewEmployeeDDLimit);// CompanyBankAccount

    ProcessResult<BPCompanyServiceInfo> updateBPLimits(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, SpcfMoney pNewCompanyDDLimit, SpcfMoney pNewPayeeDDLimit);

    ProcessResult<CompanyService> updateServiceStatus(SourceSystemCode pSourceSystemCd, String pSourceCompanyId,
                                                      ServiceCode pServiceCode, ServiceSubStatusCode pServiceSubStatusCd);

    ProcessResult<Company> addOnHoldReason(SourceSystemCode pSourceSystemCode, String pSourceCompanyId,
                                           ServiceSubStatusCode pServiceSubStatusCd);

    ProcessResult<Company> removeOnHoldReason(SourceSystemCode pSourceSystemCode, String pSourceCompanyId,
                                              ServiceSubStatusCode pServiceSubStatusCd);

    ProcessResult updateSubStatuses(SourceSystemCode pSourceSystemCd, String pSourceCompanyId,
                                    ServiceCode pServiceCd, DomainEntitySet<ServiceSubStatus> pSubStatusList);

    //******************************************************************************************
    //** Company Bank Account
    //******************************************************************************************

    ProcessResult<CompanyBankAccount> addCompanyBankAccount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyBankAccountDTO pCompanyBankAccountDTO, boolean pShouldAddRandomDebits, boolean pShouldCheckForExistingBA);

    ProcessResult<CompanyBankAccount> addCompanyBankAccount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyBankAccountDTO pCompanyBankAccountDTO, boolean pShouldAddRandomDebits, boolean pShouldCheckForExistingBA, boolean pIsPSPRandomDollarVerificationRequired);

    ProcessResult<CompanyBankAccount> updateCompanyBankAccount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyBankAccountDTO pCompanyBankAccountDTO);

    ProcessResult<CompanyBankAccount> updateCompanyBankAccountWithAccountService(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyBankAccountDTO pCompanyBankAccountDTO);

    ProcessResult<CompanyBankAccount> changeCompanyBankAccount(SourceSystemCode pSourceSystemCd, String pSourceCompanyId,
                                                               CompanyBankAccountDTO pCompanyBankAccountDTO,
                                                               boolean pShouldAddRandomDebits,
                                                               boolean pShouldAllowPendingTransactions,
                                                               boolean pShouldMovePendingTransactionsToAccount);

    ProcessResult<CompanyBankAccount> changeCompanyBankAccountWithAccountService(SourceSystemCode pSourceSystemCd, String pSourceCompanyId,
                                                               CompanyBankAccountDTO pCompanyBankAccountDTO,
                                                               boolean pShouldAddRandomDebits,
                                                               boolean pShouldAllowPendingTransactions,
                                                               boolean pShouldMovePendingTransactionsToAccount,boolean pAccountServiceUpdate,boolean isPSPRandomDollarVerificationRequired);

    ProcessResult<CompanyBankAccount> deactivateCompanyBankAccount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pSourceCompanyBankAccountId, boolean pShouldAllowPendingTransactions, boolean pIgnoreSystemCapabilityChk);

    ProcessResult<CompanyBankAccount> verifyCompanyBankAccount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pSourceCompanyBankAccountId, SpcfMoney pTxnToVerify1, SpcfMoney pTxnToVerify2, boolean mAgentVerify);

    ProcessResult<CompanyBankAccount> resetBankAccountVerifyRetryCount(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, String pSourceCompanyBankAccountId);

    ProcessResult<CompanyBankAccount> reinitiateBankAccountRandomDebits(SourceSystemCode pSourceSystemCd, String pSourceCompanyId, String pSourceCompanyBankAccountId);

    ProcessResult movePendingTransactionsToBankAccount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId,
                                                       String pSourceCompanyBankAccountId, boolean pShouldIgnoreBankAccountValidation);


    //******************************************************************************************
    //** Company Events
    //******************************************************************************************

    /**
     * Adds a Company Event and Event Details
     *
     * @param pSourceSystemCode
     * @param pSourceCompanyId
     * @param pCompanyEventDTO
     * @return
     */
    ProcessResult<CompanyEvent> addCompanyEvent(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyEventDTO pCompanyEventDTO);


    ProcessResult<CompanyEvent> addStrikeEvent(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pStrikeReason, SpcfCalendar pStrikeDate);

    ProcessResult<CompanyEvent> cancelStrikeEvent(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, SpcfUniqueId pCompanyEventId);

    ProcessResult<CompanyEventEmail> resendEmail(SourceSystemCode sourceSystemCd, String sourceCompanyId, String emailId, boolean resendRelatedEvents);

    ProcessResult<CompanyEventEmail> sendEmailToMtl(SourceSystemCode sourceSystemCd, String sourceCompanyId, String emailSeqId, boolean resendRelatedEvents, String sessionUserEmailAddress);

    ProcessResult updateCompanyAgency(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pAgencyId,
                                      CompanyAgencyDTO pCompanyAgencyDTO);

    ProcessResult<CompanyFilingAmount> addOrUpdateCompanyFilingAmount(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyFilingAmountDTO pCompanyFilingAmountDTO);

    ProcessResult<CompanyPayrollItem> addOrUpdateCompanyPayrollItem(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyPayrollItemDTO pCompanyPayrollItemDTO);

    ProcessResult<CompanyLaw> addOrUpdateCompanyLaw(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyLawDTO pCompanyLawDTO);

    ProcessResult<CompanyLaw> addOrUpdateCompanyLawRates(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pSourceLawId, List<CompanyLawRateDTO> pCompanyLawRateDTOs, boolean pIsSystemUpdate);

    ProcessResult addOrUpdateAgencyId(SourceSystemCode sourceSystemCd, String sourceCompanyId, AgencyIdDTO agencyIdDTO);

    ProcessResult<RAFEnrollment> updateRAFEnrollmentStatus(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, RAFEnrollment pExistingRAFEnrollment, RAFEnrollmentStatus pStatus);

    ProcessResult<RAFEnrollment> reInitiateRAFEnrollment(RAFEnrollment pRAFEnrollment);

    ProcessResult<RAFEnrollment> rejectRAFEnrollment(RAFEnrollment pRAFEnrollment, String pRejectReason);

    ProcessResult addACHEnrollment(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pPaymentTemplateCd, boolean pReAddIfPresent);

    ProcessResult deleteACHEnrollment(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pPaymentTemplateCd);

    ProcessResult updateACHEnrollmentStatus(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pPaymentTemplateCd, ACHEnrollmentStatus pACHEnrollmentStatus);

    ProcessResult<ACHEnrollmentFile> uploadACHResponseFile(String pFileName, String pFileContent);

    ProcessResult<EftpsEnrollment> updateEftpsEnrollment(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, EftpsEnrollmentStatus pEftpsEnrollmentStatus);
    ProcessResult<EftpsEnrollment> updateEftpsEnrollment(EftpsEnrollment pEftpsEnrollment, EftpsEnrollmentStatus pEftpsEnrollmentStatus);
    ProcessResult<EftpsEnrollment> createSecondaryEftpsEnrollment(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pFedTaxId, String pLegalName, String pLegalZip);

    ProcessResult<CheckPrintSignature> addOrUpdateCheckPrintSignature(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, byte[] pSignatureImage);

    ProcessResult<CompanyPaycheckBatch> addCheckPrintTestBatch(SourceSystemCode pSourceSystemCode, String pSourceCompanyId);

    ProcessResult<Company> updateCompanyTokensAndIdsCore(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, CompanyDTO pCompanyDTO, boolean pOverride);

    /*
      Callers must check to see if the AS/400 token has changed.
      Callers must update the AS/400 token.
     */
    ProcessResult updateDataSyncTokens(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, UpdateDataSyncTokensDTO dto);

    ProcessResult<LiabilityCheck> addOrUpdateLiabilityCheck(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, LiabilityCheckDTO pLiabilityCheckDTO);
    ProcessResult<QbdtPayrollTransaction> addOrUpdateQBDTPayrollTransaction(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, QBDTPayrollTransactionDTO pQBDTPayrollTransactionDTO);

    ProcessResult<EntityChange> addOrUpdateEntityChange(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, EntityChangeDTO pEntityChangeDTO);

    ProcessResult recalculateCompanyLedgerBalances(SourceSystemCode pSourceSystemCode, String pSourceCompanyId);

    ProcessResult generateLiabilityChecks(Company pCompany, PayrollRun pPayrollRun);
    ProcessResult generateLiabilityCheck(Company pCompany, FinancialTransaction pFinancialTransaction);

    ProcessResult<CompanyOffering> updateCompanyOffering(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, OfferingInfoDTO pOfferingInfoDTO);

    ProcessResult<Company> removeInvalidFlagOnCompanyContactsAndPayees(SourceSystemCode pSourceSystemCode, String pSourceCompanyId, String pEmailAddress);

    /**
     *
     * Used to set or update any Identity flag (OII) on Company
     *
     * @param pSourceSystemCd
     * @param pCompanyId
     * @param workflow
     * @return
     */
    ProcessResult addOrUpdateCompanyIdentity(Company pCompany, String workflow);

    public ProcessResult disassociateCompanyByPSID(String psid, SourceSystemCode sourceSystemCode,
                                                   String workOrderId, String workOrderCreatedTime);

    ProcessResult addOrUpdate401kCompanyQBDTPItem(Hcm401kCompanyPolicy hcm401kCompanyPolicy, String employerPItemId, String employeePItemId);

    ProcessResult addOrUpdate401kEmployerDeduction(Company company, EmployerDeduction employerDeduction);

    ProcessResult addOrUpdate401kEmployerPension(Company company, EmployerPension employerPension);

    ProcessResult<SMSMigrationStatus> revertSMSMigratedCompany(String psId, String tid);

    ProcessResult<SMSMigrationStatus> enableSMSMigratedFlags(String psId, String tid);

    ProcessResult<SMSMigrationStatus> migratePSPToSMS(String psId, String tid);

    ProcessResult<SMSMigrationStatus> migratePSPToSMS(String psId, String tid, boolean debugEnabled, boolean riskLmtMigrationEnabled);


}

