DECLARE
   MAX_ID NUMBER;
BEGIN
   SELECT NVL(MAX(TEST_CASE_STEP_GSEQ),0)
   INTO MAX_ID
   FROM TEST_CASE_STEP;

   MAX_ID := MAX_ID + 1;

   EXECUTE IMMEDIATE 'DROP SEQUENCE TEST_CASE_STEP_SEQ';
   EXECUTE IMMEDIATE 'CREATE SEQUENCE TEST_CASE_STEP_SEQ START WITH ' || MAX_ID || ' INCREMENT BY 1';
END;
