SELECT /*+PARALLEL(16) */ count(PAYMENT_BATCH_ASSOC_SEQ)
FROM PSPADM.PSP_PAYMENT_BATCH_ASSOC pba,
     PSPADM.PSP_MONEY_MOVEMENT_TRANSACTION mmt
WHERE pba.MONEY_MOVEMENT_TRANSACTION_FK = mmt.MONEY_MOVEMENT_TRANSACTION_SEQ
  AND pba.COMPANY_FK is not null
  and pba.COMPANY_FK = mmt.COMPANY_FK
  and pba.PAYMENT_BATCH_ASSOC_SEQ in
      (SELECT PAYMENT_BATCH_ASSOC_SEQ FROM SKUMAR71.TEMP_PSP_PAYMENT_BATCH_ASSOC);
