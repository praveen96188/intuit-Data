    rdsadmin.rdsadmin_password_verify.create_verify_function(
        p_verify_function_name => 'SOX_VERIFY_FUNCTION', 
   -- Check if the password is same as the username
    p_disallow_username    => true,
   -- 1. Check for one non-alphabetic character
    p_min_special          => 1,
   -- 2. Check for one low case character
    p_min_lowercase        => 1,
   -- 3. Check for one capital character
        p_min_uppercase        => 1, 
   -- Check if the password differs from the previous password by at least
   -- 3 letters
        p_min_different_chars  => 3,
   -- Check for the minimum length of the password
        p_min_length           => 8 ,
  -- disallow reverse of username
        p_disallow_reverse    => true,
   -- disallow simple string        
        p_disallow_simple_strings    => true,
   -- disallow white space      
        p_disallow_whitespace    => true,
   -- disallow at sign          
        p_disallow_at_sign    => true);
end;
