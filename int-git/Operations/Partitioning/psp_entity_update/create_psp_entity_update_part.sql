--create new table with range partitioning (interval)
create table PSPADM.PSP_ENTITY_UPDATE_PART
(
ENTITY_UPDATE_SEQ VARCHAR2(255 char) not null,
VERSION NUMBER(19) not null,
CREATOR_ID VARCHAR2(30 char),
CREATED_DATE TIMESTAMP(6) not null,
MODIFIER_ID VARCHAR2(30 char),
MODIFIED_DATE TIMESTAMP(6) not null,
REALM_ID NUMBER(19) default -1 not null,
ENTITY_NAME VARCHAR2(4000 char),
RETRY_COUNT NUMBER(10),
CHANGED_ATTRIBUTES CLOB,
STATUS VARCHAR2(255 char)
constraint C_ENTITY_UPDATE_STATUS
check (STATUS IN('Created', 'Published', 'Failed', 'IQFailed', 'IQPublished', 'InProgress')),
TRANSACTION_ID VARCHAR2(4000 char),
EVENT_TYPE VARCHAR2(255 char)
constraint C_ENTITY_EVENT_TYPE
check (EVENT_TYPE IN('EntityCreate', 'EntityUpdate', 'EntityDelete')),
ENTITY_ID VARCHAR2(4000 char),
primary key (ENTITY_UPDATE_SEQ, REALM_ID)
)
PARTITION BY RANGE (CREATED_DATE)
INTERVAL(NUMTOYMINTERVAL(1, 'MONTH'))
(
PARTITION ENTITY_UPDATE_M102022 VALUES LESS THAN (TO_DATE('11/01/2022', 'MM/DD/YYYY')),
PARTITION ENTITY_UPDATE_M112022 VALUES LESS THAN (TO_DATE('12/01/2022', 'MM/DD/YYYY')),
PARTITION ENTITY_UPDATE_M122022 VALUES LESS THAN (TO_DATE('01/01/2023', 'MM/DD/YYYY')),
PARTITION ENTITY_UPDATE_M012023 VALUES LESS THAN (TO_DATE('02/01/2023', 'MM/DD/YYYY'))
);

--create index
create index PSPADM.IDX_EntityUpdate_CrDate on PSPADM.PSP_ENTITY_UPDATE_PART (CREATED_DATE) local;

