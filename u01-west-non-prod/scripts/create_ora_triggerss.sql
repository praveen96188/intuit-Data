  CREATE OR REPLACE EDITIONABLE TRIGGER "PSPADM"."TRC_LEDGER_BALANCE_CALC_AI"
AFTER INSERT ON PSP_FINANCIAL_TRANS_STATE
REFERENCING NEW AS New OLD AS Old
FOR EACH ROW
DECLARE

  rt_financial_txn                 PSP_FINANCIAL_TRANSACTION%ROWTYPE;
  v_previous_balance               PSP_LEDGER_BALANCE.BALANCE_AMOUNT%TYPE;

  c_credit_cd                      CONSTANT PSP_POSTING_RULE.CREDIT_DEBIT_IND%TYPE           := 'C';
  c_debit_cd                       CONSTANT PSP_POSTING_RULE.CREDIT_DEBIT_IND%TYPE           := 'D';
  c_add_credit_cd                  CONSTANT PSP_LEDGER_ACCOUNT.BALANCE_CALCULATION_RULE%TYPE := 'CreditAddsToBalance';
  c_add_debit_cd                   CONSTANT PSP_LEDGER_ACCOUNT.BALANCE_CALCULATION_RULE%TYPE := 'DebitAddsToBalance';
  c_version_default                CONSTANT PSP_LEDGER_BALANCE.VERSION%TYPE                  := 0;
  c_realmid_default                CONSTANT PSP_LEDGER_BALANCE.REALM_ID%TYPE                 := -1;

BEGIN

  -- HISTORY
  --   04.25.2008   EMR   Created trigger

  -- DESIGN NOTES:
  --
  --   ledger balance is per day per company per ledger account
  --
  --   for each ledger affected by the posting rules
  --     using txn state and txn type
  --
  --     update ledger balance matching
  --       company
  --       ledger account
  --       trunc(date)
  --
  --     if row not found then
  --       insert new row


  -- key information is at the transaction level:
  -- tnx type, txn amount, company id


  --
  -- In optimizing the ach offload process, Tushar found out that this trigger
  -- is expensive. Because we do not really use psp_ledger_balance in real time
  -- we decided to move the ledger balance update to a daily batch job
  -- However, we still need to keep this in place for Migration purposes
  -- Ideally, this would be transformed into a sproc that the migration code calls
  --
  IF (:NEW.CREATOR_ID != 'QBOEMigrationBatchJob' AND
      :NEW.CREATOR_ID != 'AS400MigrationBatchJob') THEN
     RETURN;
  END IF;

  --
  -- All logic below only runs for migration
  --
  SELECT *
    INTO rt_financial_txn
    FROM PSP_FINANCIAL_TRANSACTION
   WHERE FINANCIAL_TRANSACTION_SEQ = :NEW.FINANCIAL_TRANSACTION_FK;


  -- get a list of ledger accounts that need to be updated for this
  -- new transaction state according to the posting rules and also
  -- determine whether to add or subtract the amount

  FOR rec_ledger_info IN (
    SELECT a.LEDGER_ACCOUNT_CD AS LEDGER_ACCT_CD,
               DECODE (
                 b.CREDIT_DEBIT_IND,
                 c_debit_cd,  DECODE (
                                a.BALANCE_CALCULATION_RULE,
                                    c_add_debit_cd,   1,
                                    c_add_credit_cd, -1
                                  ),
                 c_credit_cd, DECODE (
                                a.BALANCE_CALCULATION_RULE,
                                    c_add_debit_cd,  -1,
                                    c_add_credit_cd,  1
                                  )
               ) AS LEDGER_AMT_MULTIPLIER
      FROM PSP_LEDGER_ACCOUNT a,
             PSP_POSTING_RULE   b
     WHERE a.LEDGER_ACCOUNT_CD    = b.LEDGER_ACCOUNT_FK
         AND b.TRANSACTION_STATE_FK = :NEW.TRANSACTION_STATE_FK
       AND b.TRANSACTION_TYPE_FK  = rt_financial_txn.TRANSACTION_TYPE_FK
  )
  LOOP

    -- for each ledger account add or subtract the new transaction amount
    UPDATE PSP_LEDGER_BALANCE
       SET VERSION           = :NEW.VERSION + 1,
           MODIFIED_DATE     = :NEW.TRANSACTION_STATE_EFF_DATE,
           BALANCE_AMOUNT    = BALANCE_AMOUNT + (
                                     rt_financial_txn.FINANCIAL_TRANSACTION_AMOUNT *
                                               rec_ledger_info.LEDGER_AMT_MULTIPLIER
                                             )
     WHERE COMPANY_FK          = rt_financial_txn.COMPANY_FK
       AND LEDGER_ACCOUNT_FK   = rec_ledger_info.LEDGER_ACCT_CD
       AND TRUNC(BALANCE_DATE) = TRUNC(:NEW.TRANSACTION_STATE_EFF_DATE);

    IF (SQL%ROWCOUNT = 0) THEN

        -- compute the current balance, meaning take the balance
        -- from a previous day and start there.  Because the balance
        -- is keyed by date, the balance could be for a previous day
        -- or might not exist at all.

        BEGIN

          SELECT BALANCE_AMOUNT
              INTO v_previous_balance
              FROM PSP_LEDGER_BALANCE
         WHERE COMPANY_FK          = rt_financial_txn.COMPANY_FK
           AND LEDGER_ACCOUNT_FK   = rec_ledger_info.LEDGER_ACCT_CD
           AND TRUNC(BALANCE_DATE) = (
                     SELECT MAX(TRUNC(BALANCE_DATE))
                         FROM PSP_LEDGER_BALANCE
                  WHERE COMPANY_FK          = rt_financial_txn.COMPANY_FK
                    AND LEDGER_ACCOUNT_FK   = rec_ledger_info.LEDGER_ACCT_CD
                     );

        EXCEPTION
          WHEN NO_DATA_FOUND THEN
              v_previous_balance := 0;
            WHEN OTHERS THEN
              RAISE;
        END;

        -- insert a new balance record for the date the tnx state changed.
        -- please note that if two inserts arrive at the same time one
        -- will win and the other will be blocked and will eventually get
        -- the standard typical duplicate pk.  At that time then an update
        -- will be needed.

        BEGIN

        INSERT INTO PSP_LEDGER_BALANCE (
          LEDGER_BALANCE_SEQ,
              VERSION,
              CREATED_DATE,
          MODIFIED_DATE,
              REALM_ID,
              BALANCE_AMOUNT,
          BALANCE_DATE,
              LEDGER_ACCOUNT_FK,
              COMPANY_FK
            )
        VALUES (
            FN_FORMAT_SYSGUID(SYS_GUID()),
          c_version_default,
          SYS_EXTRACT_UTC(SYSTIMESTAMP),
          SYS_EXTRACT_UTC(SYSTIMESTAMP),
          c_realmid_default,
              v_previous_balance +
          (
               rt_financial_txn.FINANCIAL_TRANSACTION_AMOUNT *
               rec_ledger_info.LEDGER_AMT_MULTIPLIER
              ),
          :NEW.TRANSACTION_STATE_EFF_DATE,
          rec_ledger_info.LEDGER_ACCT_CD,
          rt_financial_txn.COMPANY_FK
          );

        EXCEPTION
          WHEN DUP_VAL_ON_INDEX THEN

          UPDATE PSP_LEDGER_BALANCE
             SET VERSION           = :NEW.VERSION + 1,
                 MODIFIED_DATE     = :NEW.TRANSACTION_STATE_EFF_DATE,
                 BALANCE_AMOUNT    = BALANCE_AMOUNT + (
                                             rt_financial_txn.FINANCIAL_TRANSACTION_AMOUNT *
                                                       rec_ledger_info.LEDGER_AMT_MULTIPLIER
                                                     )
           WHERE COMPANY_FK          = rt_financial_txn.COMPANY_FK
             AND LEDGER_ACCOUNT_FK   = rec_ledger_info.LEDGER_ACCT_CD
             AND TRUNC(BALANCE_DATE) = TRUNC(:NEW.TRANSACTION_STATE_EFF_DATE);

            WHEN OTHERS THEN
              RAISE;
        END;

      END IF;

  END LOOP;

EXCEPTION
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR (
        -20501,
        'Error updating ledger balance for transaction ' ||
          :NEW.FINANCIAL_TRANSACTION_FK                  ||
          ' for txn state '                              ||
          :NEW.TRANSACTION_STATE_FK                      ||
          '. '                                           ||
          SQLERRM,
        FALSE
      );
END;




