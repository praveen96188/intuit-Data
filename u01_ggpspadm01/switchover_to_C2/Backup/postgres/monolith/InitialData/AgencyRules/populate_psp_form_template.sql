 FROM TMP_PSP_FORM_TEMPLATE tt WHERE tt.FORM_TEMPLATE_CD = rt.FORM_TEMPLATE_CD)
 WHERE rt.FORM_TEMPLATE_CD IN (SELECT FORM_TEMPLATE_CD FROM TMP_PSP_FORM_TEMPLATE)
;



